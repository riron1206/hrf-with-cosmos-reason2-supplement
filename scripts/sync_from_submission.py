import os
import re
import urllib.request

SUBMISSION_REPO = "turingmotors/hrf-with-cosmos-reason2"
SUBMISSION_BRANCH = "main"
SOURCE_MD_PATH = "project_features.md"

RAW_BASE = f"https://raw.githubusercontent.com/{SUBMISSION_REPO}/{SUBMISSION_BRANCH}"
BLOB_BASE = f"https://github.com/{SUBMISSION_REPO}/blob/{SUBMISSION_BRANCH}"

def fetch_text(url: str) -> str:
    with urllib.request.urlopen(url) as r:
        return r.read().decode("utf-8")

def rewrite_relative_links(md: str) -> str:
    """Rewrite relative URLs so the rendered site can resolve assets and files.

    - Images: ![alt](relative.png) -> ![alt](RAW_BASE/relative.png)
    - Links:  [text](relative.md)  -> [text](BLOB_BASE/relative.md)
    """

    def is_relative(url: str) -> bool:
        return (not re.match(r"^(https?:)?//", url)
                and not url.startswith("#")
                and not url.startswith("mailto:"))

    # Images: ![...](...)
    def repl_img(m):
        alt, url = m.group(1), m.group(2).strip()
        if is_relative(url):
            url = f"{RAW_BASE}/{url.lstrip('./')}"
        return f"![{alt}]({url})"

    md = re.sub(r"!\[([^\]]*)\]\(([^)]+)\)", repl_img, md)

    # Links (exclude images): [...](...)
    def repl_link(m):
        text, url = m.group(1), m.group(2).strip()
        if is_relative(url):
            url = f"{BLOB_BASE}/{url.lstrip('./')}"
        return f"[{text}]({url})"

    md = re.sub(r"(?<!\!)\[([^\]]+)\]\(([^)]+)\)", repl_link, md)
    return md

def main():
    src_url = f"{RAW_BASE}/{SOURCE_MD_PATH}"
    md = fetch_text(src_url)

    md = rewrite_relative_links(md)

    header = f"""<!--
AUTO-GENERATED.
Source: {BLOB_BASE}/{SOURCE_MD_PATH}
Do not edit this file directly.
-->

"""
    out = header + md

    os.makedirs("docs", exist_ok=True)
    with open("docs/index.md", "w", encoding="utf-8") as f:
        f.write(out)

    print("Wrote docs/index.md from", src_url)

if __name__ == "__main__":
    main()
